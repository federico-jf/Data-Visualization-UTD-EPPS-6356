#Plotting functions
par() # If you set parameter values here, the changes will be in effect 
#for the rest of the session or until you change them again.
lines() #draw lines, define color of line, type of line, width
points() # draw points, determine x, y, pch, color of points
axis()#draw axis, for example, > axis(2, at=seq(0, 100, 20)) draw axis 2 sequence 0-100 each 20
box()# draw the box of the plot, you can define color of line and lwd: box(col="grey", lwd=1)
text()# add text at specific ubication (x,y). For example: text(150, 600,
#"Pressure (mm Hg)\nversus\nTemperature (Celsius)")
mtext()# The mtext() function draws text at any location in any of the margins. For exmaple:
#mtext("Left end of margin", adj=0, side=3) is for adding "Lefts and in the margin" with justification
# 0, that is left hand and in the side 3 of the plot.
hist()# create a histogram
boxplot()#create a boxplot
legend() #draw a legend outside the plot region
#for example
with(iris,
       plot(Sepal.Length, Sepal.Width,
            pch=as.numeric(Species), cex=1.2))
legend(6.1, 4.4, c("setosa", "versicolor", "virginica"),
         cex=1.5, pch=1:3)
persp()#create 3d plot
names() #names is a generic accessor function to the names attribute of 
#an R object, typically a vector.
pie()#create a pie chart
